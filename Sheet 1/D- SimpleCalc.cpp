#include <iostream>
using namespace std;

int main(){
      int k;
      long long a;
      cin >> k >> a;
      cout << k << " + " << a << " = " << k+a << '\n'; 
      cout << k << " * " << a << " = " << k*a << '\n'; 
      cout << k << " - " << a << " = " << k-a << '\n'; 

      return 0;
}